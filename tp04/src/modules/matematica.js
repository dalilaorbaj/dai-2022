export function sumar (n1, n2){
    let rta = n1+n2;
    return rta; 
}

export const restar = (n1, n2) => n1-n2;

export const dividir = (n1, n2) => n1/n2;

export const multiplicar = (n1, n2) => n1*n2;

export const potenciar = (n1) => n1*n1;

export const raiz = (n1) => Math.sqrt(n1);

const pi = 3.14
export const perimetroCircunferencia = (radio) => pi*2*radio; 